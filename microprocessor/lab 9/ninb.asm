TITLE DISPLAYING AT THE CENTER
.MODEL SMALL
.STACK 100H
.DATA
STRLEN DB 60
STRSZ DB ?
STR DB 60 DUP('$')

.CODE
MAIN PROC FAR
    MOV AX, @DATA
    MOV DS, AX

    MOV AH, 0AH
    LEA DX, STRLEN
    INT 21H

    MOV AH, 00
    MOV AL, 03H
    INT 10H

    MOV BH, 00
    MOV DH, 12H
    MOV AL, 80
    MOV DL, STRSZ
    SUB AL, DL
    MOV DL, 02H
    DIV DL
    MOV DL, AL
    MOV AH, 02H
    INT 10H

    LEA DX, STR
    MOV AH, 09H
    INT 21H

    MOV AX, 4C00H
    INT 21H
MAIN ENDP
END MAIN
